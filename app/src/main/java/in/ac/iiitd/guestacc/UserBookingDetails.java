package in.ac.iiitd.guestacc;



public class UserBookingDetails {

    String from_date;
    String number_of_persons;
    String number_of_rooms;
    String raised_on;
    String status;
    String to_date;
    String total_amount;

    public String getFrom_date() {
        return from_date;
    }

    public void setFrom_date(String from_date) {
        this.from_date = from_date;
    }

    public String getNumber_of_persons() {
        return number_of_persons;
    }

    public void setNumber_of_persons(String number_of_persons) {
        this.number_of_persons = number_of_persons;
    }

    public String getNumber_of_rooms() {
        return number_of_rooms;
    }

    public void setNumber_of_rooms(String number_of_rooms) {
        this.number_of_rooms = number_of_rooms;
    }

    public String getRaised_on() {
        return raised_on;
    }

    public void setRaised_on(String raised_on) {
        this.raised_on = raised_on;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTo_date() {
        return to_date;
    }

    public void setTo_date(String to_date) {
        this.to_date = to_date;
    }

    public String getTotal_amount() {
        return total_amount;
    }

    public void setTotal_amount(String total_amount) {
        this.total_amount = total_amount;
    }

}
